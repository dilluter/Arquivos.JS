// Math.round() - Arredonda para o inteiro mais próximo
let numero1 = 4.5;
let arredondado1 = Math.round(numero1); // Resultado: 5
console.log("Math.round(4.5) =", arredondado1);

// Math.floor() - Arredonda para baixo (menor inteiro)
let numero2 = 4.9;
let arredondado2 = Math.floor(numero2); // Resultado: 4
console.log("Math.floor(4.9) =", arredondado2);

// Math.ceil() - Arredonda para cima (maior inteiro)
let numero3 = 4.1;
let arredondado3 = Math.ceil(numero3); // Resultado: 5
console.log("Math.ceil(4.1) =", arredondado3);

// Math.random() - Gera um número aleatório entre 0 (inclusivo) e 1 (exclusivo)
let numeroAleatorio = Math.random(); // Exemplo de resultado: 0.7398
console.log("Math.random() =", numeroAleatorio);

// Para gerar um número aleatório inteiro entre 0 e 10, por exemplo:
let aleatorioEntre0e10 = Math.floor(Math.random() * 11); // 0 a 10
console.log("Número aleatório entre 0 e 10 =", aleatorioEntre0e10);

 